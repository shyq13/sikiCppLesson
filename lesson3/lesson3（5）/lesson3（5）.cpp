using namespace std;

int main() {
	//认识指针
	int a = 10;
	float b = 3.14;
	//&（and符号）可以取得一个变量的内存地址
	cout << "变量a的内存地址是" << &a << endl;   //获取变量a的内存地址
	cout << "变量b的内存地址是" << &b << endl;   //获取变量b的内存地址
	//*（星号）可以根据一个内存地址取得数据
	cout << "变量a的值是" << *(&a) << endl;   //获取变量a的值
	cout << "变量b的值是" << *(&b) << endl;   //获取变量b的值

	
	return 0;
}